public class Animal {
private String name;
private String ranger;
private String status;
public Animal(String name,String ranger,String status){
        this.name=name;
        this.ranger=ranger;
        this.status=status;
}

public String getName(){
        return name;
}
public String getRanger(){
        return ranger;
}
public String getStatus(){
        return status;
}
}
